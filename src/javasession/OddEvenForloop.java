package javasession;

public class OddEvenForloop {

	public static void main(String[] args) {
		//  print all odd and even numbers between 1 to 100
		
		for(int i=1;i<=100;i++)
		{
			if(i%2==0)
			System.out.println("Even Number: "+i);
			else
			System.out.println("Odd Number: "+i);
				
		}
	}

}
